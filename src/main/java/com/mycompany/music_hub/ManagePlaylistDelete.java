    public void setSongDetailsToTable() {
        try {
            Connection conn = DBConnection.getConnection();
            String query = "SELECT * FROM songs WHERE user_id = ?";
            PreparedStatement pstmt = conn.prepareStatement(query);
            pstmt.setInt(1, userId);
            ResultSet rs = pstmt.executeQuery();
            
            model = (DefaultTableModel) tbl_songs.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                int songId = rs.getInt("song_id");
                String song = rs.getString("song_name");
                String artist = rs.getString("artist");

                Object[] row = { songId, song, artist };
                model.addRow(row);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }   
    }
    
    public boolean deleteSong(int songId) {
        boolean isDeleted = false;

        try {
            Connection conn = DBConnection.getConnection();
            String query = "DELETE FROM songs WHERE song_id = ?";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setInt(1, songId);

            int rowCount = ps.executeUpdate();
            if (rowCount > 0) {
                isDeleted = true;
            } else {
                isDeleted = false;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

        return isDeleted;
    }

