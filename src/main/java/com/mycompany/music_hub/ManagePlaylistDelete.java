    private void rSMaterialButtonCircle3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonCircle3ActionPerformed
        String songIdStr = txt_id.getText().trim();
    if (!songIdStr.isEmpty()) {
        try {
            int songId = Integer.parseInt(songIdStr);
            boolean isDeleted = deleteSong(songId);
            
            if (isDeleted) {
                setSongDetailsToTable();
                updateRowCountLabel();
                txt_id.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Song ID not found or could not be deleted.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid song ID.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please enter a song ID.");
    }
    }//GEN-LAST:event_rSMaterialButtonCircle3ActionPerformed

